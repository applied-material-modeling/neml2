// Client-side "you are viewing an old/dev version" banner for the multi-version
// documentation site (gh-pages layout: /<repo>/{stable,vX.Y,dev,pr-preview/N}/).
//
// Why client-side and not a build-time `announcement`: once a version's site is
// deployed it is NEVER rebuilt, so a static banner baked at build time could not
// retroactively flag a site as out-of-date when a newer release ships later.
// Instead this script reads the single source of truth -- /<repo>/versions.json
// (regenerated by doc/scripts/deploy_versioned.sh on every release) -- and
// compares the version segment in the current URL against `stable`.
//
// Loaded as a classic <script> (NOT type="module"): the doc-render validator
// (doc/scripts/check_doc_render.py) opens built pages over file://, where
// browsers block ES modules under the CORS "null origin" rule. Wrapped in an
// IIFE so nothing leaks to the global scope.
//
// CRITICAL: this must never raise or log an error. The validator fails the CI
// build on any JS console error, and the versions.json fetch necessarily fails
// on a local/file:// build (no manifest, wrong origin). Every failure path is
// swallowed and simply yields no banner.

(() => {
"use strict";

// A frozen older-minor folder, e.g. "v3.0". The newest release lives at
// "stable/" (no version segment of its own), so it is matched separately.
const VERSION_SEG_RE = /^v\d+\.\d+$/;

function staleBanner(base, parts, segment, stable) {
  // Link to the same page under stable/ so the reader keeps their place; if that
  // page was removed in the newer release the host serves its 404 (acceptable).
  const rest = parts.slice(2).join("/");
  return {
    href: base + "stable/" + rest,
    text:
      "You are viewing the documentation for NEML2 " +
      segment +
      ". The latest release is " +
      stable +
      ". ",
    link: "View the latest version",
    dev: false,
  };
}

function devBanner(base) {
  return {
    href: base + "stable/",
    text:
      "This is the in-development documentation, built from the main branch " +
      "(not a released version). ",
    link: "View the latest release",
    dev: true,
  };
}

function render(banner) {
  if (document.querySelector(".neml2-version-banner")) return;
  const bar = document.createElement("div");
  bar.className = "neml2-version-banner" + (banner.dev ? " is-dev" : "");
  bar.setAttribute("role", "note");

  const span = document.createElement("span");
  span.textContent = banner.text;

  const a = document.createElement("a");
  a.href = banner.href;
  a.textContent = banner.link;

  bar.appendChild(span);
  bar.appendChild(a);
  document.body.insertBefore(bar, document.body.firstChild);
}

function bootstrap() {
  // Only meaningful when served over http(s) from the versioned gh-pages layout.
  if (location.protocol !== "http:" && location.protocol !== "https:") return;

  const parts = location.pathname.split("/").filter(Boolean);
  if (parts.length < 2) return;
  const repo = parts[0];
  const segment = parts[1];

  // stable (the newest release) and PR previews never get a staleness banner.
  if (segment === "stable" || segment === "pr-preview") return;
  const isDev = segment === "dev";
  const isVersion = VERSION_SEG_RE.test(segment);
  if (!isDev && !isVersion) return; // e.g. a local root build -- no banner

  const base = "/" + repo + "/";
  fetch(base + "versions.json", { cache: "no-cache" })
    .then((res) => (res.ok ? res.json() : null))
    .then((data) => {
      if (!data || !data.stable) return;
      if (isVersion) {
        if (segment === data.stable) return; // this IS the latest
        render(staleBanner(base, parts, segment, data.stable));
      } else {
        render(devBanner(base));
      }
    })
    .catch(() => {}); // file://, offline, or missing manifest -> no banner
}

// Classic scripts may run before the DOM is parsed, so self-defer.
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", bootstrap);
} else {
  bootstrap();
}
})();
